def mainMenu():
    print()
    print(" _________________Main Menu_________________")
    print(
        "  ||   Select:                           ||\n  ||   1 - Etch Sketch                   || \n  ||   2 - Display Object                ||\n  ||   3 - exit                          ||")
    print(" ___________________________________________")
    option = input()

    if option == "1":
        lcd.clear()
        lcd.show()
        bg = "Etch a Sketch"

        xPoint = int(input("Input the x coordinate (0 to 127):"))
        yPoint = int(input("Input the y coordinate (0 to 63):"))

        labLibrary1.displayText(bg, lcd, 20, 20)
        labLibrary1.etchSketch(xPoint, yPoint)

        mainMenu()
    elif option == "2":
        lcd.clear()
        lcd.show()
        print("Choose: \n 1 - to draw a Pac Man \n 2 - to draw fight \n 3 - to draw a snowflake: ")
        obj = int(input())
        if obj == 1:
            obj = pm
        elif obj == 2:
            obj = f1
        elif obj == 3:
            obj = snowFlake
        else:
            print("Choose a valid option")
            print("Choose: \n 1 - to draw a Pac Man \n 2 - to draw fight \n 3 - to draw a snowflake: ")
            obj = int(input())
        for y1 in obj:
            for x1 in y1:
                lenthX = len(y1)
                lenthY = len(obj)

        xPoint = int(input("Input the x coordinate (0 to 127):"))
        yPoint = int(input("Input the y coordinate (0 to 63):"))

        totalX  = lenthX + xPoint
        totalY = lenthY + yPoint

        if totalX > 127:
            print("X value to big for the image, please type a smaller X coordinate!")
            xPoint = int(input("Input the x coordinate (0 to 127):"))
            labLibrary1.displayObject(obj, xPoint, yPoint)

        elif totalY > 63:
            print("Y value to big for the image, please type a smaller Y coordinate!")
            yPoint = int(input("Input the y coordinate (0 to 63):"))
            labLibrary1.displayObject(obj, xPoint, yPoint)

        else:
            labLibrary1.displayObject(obj, xPoint, yPoint)
        mainMenu()

    elif option == "3":
        lcd.clear()
        lcd.show()
        labLibrary1.clearBacklight()
        exit
    else:
        print("Please choose a valid option!")
        mainMenu()

labLibrary1.setLight(141, 120, 30)

